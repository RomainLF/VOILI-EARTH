const models = require("../models");

class MainProduitsController {
  static browse = (req, res) => {
    models.MainProductsMen.findAll()
      .then(([rows]) => {
        res.send(rows);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static read = (req, res) => {
    models.MainProductsMen.find(req.params.id)
      .then(([rows]) => {
        if (rows[0] == null) {
          res.sendStatus(404);
        } else {
          res.send(rows[0]);
        }
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static edit = (req, res) => {
    const MainProductsMen = req.body;

    // TODO validations (length, format...)

    MainProductsMen.id = parseInt(req.params.id, 10);

    models.MainProductsMen.update(MainProductsMen)
      .then(([result]) => {
        if (result.affectedRows === 0) {
          res.sendStatus(404);
        } else {
          res.sendStatus(204);
        }
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static add = (req, res) => {
    const MainProductsMen = req.body;

    // TODO validations (length, format...)

    models.MainProductsMen.insert(MainProductsMen)
      .then(([result]) => {
        res.status(201).send({ ...MainProductsMen, id: result.insertId });
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static delete = (req, res) => {
    models.MainProductsMen.delete(req.params.id)
      .then(() => {
        res.sendStatus(204);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };
}

module.exports = MainProduitsController;
